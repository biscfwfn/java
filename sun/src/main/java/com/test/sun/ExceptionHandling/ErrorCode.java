package com.test.sun.ExceptionHandling;

public enum ErrorCode {
    TEST("500","操作失败！");

    private String code;
    private String message;

    ErrorCode(String code, String message) {
        this.code = code;
        this.message = message;
    }

    public String getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }
}
